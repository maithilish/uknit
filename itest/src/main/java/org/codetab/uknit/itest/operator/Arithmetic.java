package org.codetab.uknit.itest.operator;

class Arithmetic {

    public int add() {
        int result = 1 + 2;
        return result;
    }

    public int subtract() {
        int result = 1 - 2;
        return result;
    }

    public int multiply() {
        int result = 2 * 3;
        return result;
    }

    public int divide() {
        int result = 8 / 2;
        return result;
    }

    public int reminder() {
        int result = 9 % 2;
        return result;
    }

    public String concat() {
        String ab = "a" + "b";
        return ab;
    }
}
