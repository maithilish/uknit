package org.codetab.uknit.itest.ret;

/**
 * Arithmetic operators in return statement
 * @author Maithilish
 *
 */
class Arithmetic {

    public int add() {
        return 1 + 2;
    }

    public int substract() {
        return 1 - 2;
    }

    public int multiply() {
        return 2 * 3;
    }

    public int divide() {
        return 6 / 2;
    }

    public int reminder() {
        return 9 % 2;
    }

    public String concat() {
        return "a" + "b";
    }
}
